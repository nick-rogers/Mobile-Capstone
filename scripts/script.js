var objectOpen = false;

function toggleOverflow (){

	if(objectOpen){
		$('body').css('overflow-y', 'scroll');
		objectOpen = false;
	}else {
		$('body').css('overflow-y', 'hidden');
		objectOpen = true;
	}

};



var navVis = true;
function hideShowNav (){
	if(navVis) {
		$('.nav-wrap').css('display', 'none');
		navVis = false;
	} else {
		$('.nav-wrap').css('display', 'block');
		navVis = true;
	}

}









